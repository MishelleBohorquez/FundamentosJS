// String
let nombre = "Camila";
let ciudad = 'Medellín';
console.log(nombre, ciudad);

// Number
let cantidad = 3;
let precio = 1500.5;
console.log(cantidad + precio);

// Operadores aritmeticos
console.log(10+5);
console.log(10-5);
console.log(10/5);
console.log(10*5);

let numero1 = 1;
let numero2 = 1;

console.log(numero1 + numero2);
console.log(numero1 - numero2);
console.log(numero1 * numero2);
console.log(numero1 / numero2);

// Boolean
let pagoAprobado = true;
let carritoVacio = false;
console.log(pagoAprobado, carritoVacio);

// Undefined
let telefono;
console.log("Undefined", telefono);

// Null
let descuento = null;
console.log(descuento);

//Typeof
console.log(typeof "Camila");   // string
console.log(typeof 3000);       // number
console.log(typeof true);       // boolean
console.log(typeof undefined);  // undefined